def convert_img_to_npy(case, n, tuples):
    for i in range(1, n+1):
        l = []

        low, high = tuples[i]

        img_loc = img_src.format(case, case, i)
        for j in range(low, high + 1):
            file = img_loc + f"{case}_{i}_img{j}.jpg"
            img = mpimg.imread(file)
            print(f'{case}-{i}-img{j} loaded.')
            l.append(img)
        d = np.array(l)
        #dv.Show(d)
        npy_save = npy_targ.format(case) + f'{case}-data{i}.npy'
        np.save(npy_save, d)
        print(f'{case}-{i} npy file saved.')
    return

